unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int				length;
	unsigned int	i;

	length = 0;
	i = 0;
	while (src[length] != '\0')
		length++;
	if (0 < size)
	{
		while (i < size - 1 && src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (length);
}
