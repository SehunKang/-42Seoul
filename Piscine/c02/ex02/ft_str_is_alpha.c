int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 65 || (90 < str[i] && str[i] < 97))
			return (0);
		else if (122 < str[i])
			return (0);
		i++;
	}
	return (1);
}
