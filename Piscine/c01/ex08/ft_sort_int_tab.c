#include <stdio.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int temp;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (tab[j] > tab[j + 1])
			{
				temp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = temp;
			}
			j++;
		}
		i++;
	}
}

int	main()
{
	int str[] = {1, -10, 0, 10, 100};
	ft_sort_int_tab(str, 4);
	printf("%d %d %d %d %d", str[0], str[1], str[2], str[3], str[4]);
}
