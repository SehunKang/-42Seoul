#include <stdio.h>
char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
int		main(void)
{
	char a[] = "hel";
	char b[] = "apple";

	ft_strcpy(a, b);
	printf("%s\n", a);
}

